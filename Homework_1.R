## üìÑ Contents

### `HOMEWORK_1.R`
- Full R script detailing exploratory data analysis, linear modeling with interaction terms, and visualization using `ggplot2`.
- Includes model fitting, prediction, confidence intervals, and mean squared error analysis.
- Investigates how BMI and smoking status interact to affect medical charges.
- Concludes with bias-variance tradeoff analysis, including simulation and extra credit exercises on estimating bias and variance through repeated sampling.

### `HOMEWORK_2_KA.pdf`
- PDF document exploring classification algorithms and logistic regression techniques.
- Applied ROC curve analysis and evaluated model calibration.
- Explains diagnostic metrics such as precision, recall, F1-score, and AUC.

### `HOMEWORK_3_KA.pdf`
- Decision trees and random forests assignment.
- Evaluated model interpretability using feature importance plots.
- Includes tuning of hyperparameters (e.g., `max_depth`, `min_samples_split`) and performance assessment on test sets.

### `HOMEWORK_4_KA.pdf`
- Regularization techniques in high-dimensional settings.
- Employed Ridge, Lasso, and Elastic Net regression models.
- Includes cross-validation for model selection and discussion on sparsity, model shrinkage, and predictive performance.

